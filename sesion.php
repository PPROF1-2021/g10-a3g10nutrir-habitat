      <h1>Bienvenido</h1>

   <p> A continuación, te damos las claves de los dos aspectos principales que te permitirán ver resultados: un plan alimentario adecuado y sumar actividad física a nuestra vida.</p>

   <p>Solo los cambios de hábitos alimentarios graduales y sostenibles en el tiempo son los que nos van a permitir sacarnos esos kilitos de más y sostener esa baja de peso en el largo plazo.
Nuestros especialistas en nutrición elaboraron un plan alimentario saludable para comenzar a bajar de peso, desintoxicar el organismo y, fundamentalmente, comenzar a adquirir cambios positivos en la alimentación. </p>
   <p>Lo ideal es aumentar el consumo de alimentos frescos como frutas y verduras; acompañándolos con proteínas magras: carne y pollo, retirando la grasa visible incluso antes de cocinar.</p>

   <p> Es importante aumentar el consumo de pescado y preparaciones con claras de huevo </p>

  <p>Hacer una buena selección de hidratos de carbono; elegir cereales integrales, legumbres, avena, frutas y evitar el consumo de harinas refinadas o blancas y de las carbograsas; galletitas, bizcochos, medialunas, masas de pizza y empanadas, tartas, pastas rellenas, rebozados (en especial fuera de tu casa) y prefritos. Así también, debemos disminuir consumo de alcohol, bebidas azucaradas y grasas malas como manteca, crema, achuras. Las grasas de buena calidad como frutos secos (almendras, nueces, etc.) es bueno incorporarlas, teniendo en cuenta de no excedernos en cantidad, por su valor calórico.</p>
  
  <p>En los almuerzos y cenas, debemos disminuir o evitar las harinas blancas o refinadas; y aumentar el consumo de proteínas magras y verduras y frutas. Que nuestro plato siempre tenga color. Si nuestros platos son por lo general sin mucho color, ej.: carne con papas o arroz; empanadas, pizza, pastas, estamos perdidas desde el punto de vista nutricional.</p>

  <p>Otro dato a tener muy en cuenta es no dejar pasar más de 4 horas sin comer, para evitar llegar a la próxima comida muertas de hambre y ahí probablemente nuestro cuerpo nos pida dulces y harinas en vez de alimentos saludables.</p>

  <p>Es fundamental aumentar consumo de agua durante el día. Debemos arrancar y terminar el día ingiriendo agua. A los que les cuesta consumir agua, podemos reemplazarlas por aguas saborizadas realizadas por nosotros, ej.: agua con rodajitas de limón o/y naranja/pomelo, pepino y jengibre. Tenemos que volver a lo natural, y disminuir el consumo de jugos comerciales y gaseosas, aun las light, que aunque no tienen calorías, tienen mucho sodio y además estimulan las ganas de comer dulce luego.</p>

    <h2>Plan nutricional genérico</h2>

<p>Desayunos y meriendas saludables:</p>

<p> 1.Licuado de pera y espinaca: Colocar en licuadora 1-2 peras + 1 manojo de espinaca cruda fresca. Opcional: se puede agregar al licuado algunas almendras o 1 cucharada de semillas y edulcorante (preferentemente stevia). Agregar agua hasta tapar los ingredientes y licuar.</p>
<p>2. Infusiones: Ej.: té verde con un poquito de jengibre rallado + 2-3 galletas de arroz con queso blanco descremado + opcional yogurt descremado o natural.</p>
<p>3. Panqueque mezclando siguientes ingredientes: 2-3 claras de huevo + 1 cucharada de avena instantánea + trocitos de frutilla + esencia de vainilla y opcional una pisca de polvo de hornear. Cocinar en sartén. Puede acompañar con infusión té verde o agua saborizada natural con rodaja de limón, pepino y jengibre.</p>

<p>Almuerzos:
1. Omelette Capresse: con 1 yema + 2-3 claras o solo claras + 1 feta de queso magro + tomate y albahaca. Puede acompañar con ensalada repollo y zanahoria.
2. Wraps relleno de verduritas al wok con lomo o pollo: Usar como wrap la lechuga capuchina.
3. Ensalada verdes, tomatitos cherry + atun o salmón. Opcional: puede agregar 2 cucharadas de choclo o arroz integral .</p>

<p>Cenas:
1. Salmón a la plancha con ensalada de hojas verdes.
2. Ensalada tibia de vegetales: como morrones, cebolla de verde, zuchinis y berenjenas asados y sus cus.
3. Zapallitos rellenos de carne picada magra y vegetales. Gratinar al horno con feta fina de queso magro.</p>

<h2>Rutina de entrenamiento genérica.</h2>

 <p>Lo más importante es elegir una actividad con la que nos encontremos a gusto, para no abandonarla con el correr del tiempo, y que además, podamos realizar con una cierta continuidad, esto sería de 3 a 4 veces por semana. Lo ideal es ir alternando el entrenamiento con diferentes deportes o también podemos combinarlos con los ejercicios que más nos gusten, siempre con el objetivo de no aburrirnos o cansarnos". 
Entre las opciones que podemos elegir están andar en bicicleta o spinning, running, natación, pesas, aeróbic, step, cardiobox, elíptica, aquafitness. En general, cualquier actividad deportiva nos hará bien. </p>


  <p>Deportes recomendados</p>

<p>Correr es la opción más fácil para cualquier persona, no necesitamos acceder a un gimnasio, por ejemplo, para poder llevar adelante esta actividad. Solo tenemos que ponernos ropa cómoda y adecuada, zapatillas y salir a la calle o a un espacio al aire libre. Al practicar este deporte tenemos que tener cuidado con las lesiones, y siempre debemos optar por correr en terrenos blandos como el césped, para disminuir los efectos del rebote. Las superficies duras, como el asfalto, no son recomendables.</p>
<p>La bicicleta tiene un riesgo menor de lesión en relación a correr y casi todos tenemos una en nuestra casa. ¿Por qué no usarla para ir al trabajo? ¿Para salir a pasear? Andar en bicicleta implica utilizar los músculos más importantes del cuerpo y alterna, períodos activos con momentos de descanso. Es una actividad muy completa: quema calorías; reduce el colesterol, la flacidez y la celulitis; fortalece la musculatura, disminuye la presión arterial, mejora las funciones del sistema circulatorio, entre otros beneficios.</p> 
<p>La natación también es una actividad muy recomendable para estas épocas y es una de las más completas, ya que se trabajan múltiples músculos, tanto en el tren inferior como en el tren superior del cuerpo. Se queman grasas, se tonifica músculos, y ayuda a prevenir la flacidez. También, se obtienen buenos resultados, en poco tiempo, a nivel cardiovascular.</p> 
<p>Levantar pesas.</p><p> Si bien estaba considerado un ejercicio de hombres, esto ha cambiado con el tiempo. Levantar pesas es tanto para hombres como para mujeres, y trae muchos beneficios a nuestra salud. Es una buena manera de trabajar y tonificar todos los grupos musculares, incrementa el gasto calórico, mejora la circulación sanguínea, reducir el colesterol, entre otros beneficios.</p> 
<p>Participar de clases colectivas.</p><p> Las clases colectivas, también pueden ser nuestras aliadas a la hora de ponernos en forma, ya que es una manera divertida de ejercitarnos. En casi todas ellas vamos a trabajar de igual manera el componente cardiovascular y muscular.</p> 
<p>Ejercicios de estiramientos.</p><p> Los estiramientos forman parte de un ejercicio completo, deben ser parte de nuestra rutina. Con ellos conseguiremos unos músculos más flexibles, con mayor movimiento, y menos propensos a sufrir lesiones. Con unos 10, 15 minutos para estirar tu musculatura, alcanza y claro, obtendremos numerosos beneficios.</p>


